      <h1 class="fw-bold">Komponen Rem Cakram Mobil Beserta Fungsinya</h1>
      <img src="../../assets/gambar/berita4.webp" alt="Ilustrasi sistem rem cakram mobil lengkap dengan piringan cakram" loading="lazy" class="img-fluid" style="max-width: 700px; height: auto;">
      <p class="lead">Written by: Shalsabilla Jana</p>
      <p class="text-start">Sistem pengereman merupakan salah satu elemen paling krusial dalam Kaki-Kaki mobil. Di antara berbagai jenis sistem rem, Rem Cakram (Disc Brake) adalah yang paling umum digunakan pada mobil modern, khususnya pada roda depan, dan kini juga mulai diterapkan pada roda belakang. Terdiri dari beberapa komponen Rem Cakram mobil yang bekerja sama menghasilkan performa pengereman yang baik, responsif, dan tahan terhadap panas berlebih.</p>
      <p class="text-start">Dalam artikel ini, kita akan membahas secara rinci mengenai komponen Rem Cakram pada mobil beserta fungsinya dalam menghentikan kendaraan dengan aman dan efisien.</p>
      
      <h2 class="text-start">Komponen Rem Cakram Mobil & Fungsinya</h2>
      <p class="text-start">Dalam Sparepart mobil, sistem pengereman pun disusun oleh beberapa komponen, baik itu Rem Cakram maupun Rem Tromol. Berikut adalah komponen Rem Cakram mobil lengkap dengan fungsinya:</p>
      <h3 class="text-start">1. Cakram (Disc Brake)</h3>
      <p class="text-start">Cakram atau disc rotor adalah komponen Rem Cakram mobil yang paling utama. Berbahan logam yang berbentuk piringan, cakram terpasang pada hub roda dan berputar bersama roda. Komponen ini menjadi media yang dijepit oleh Kampas Rem untuk menghasilkan gaya gesek. Gesekan inilah yang mengubah energi kinetik kendaraan menjadi panas dan memperlambat putaran roda. Terdapat 3 tipe cakram atau disc, yaitu:</p>
      <p class="text-start">• Solid Disc: Piringan cakram utuh dengan ketebalan tinggi, biasanya untuk mobil kecil atau roda belakang.</p>
      <p class="text-start">• Ventilated Disc: Piringan ini memiliki celah-celah di dalamnya untuk meningkatkan sirkulasi udara dan mencegah overheating.</p>
      <p class="text-start">• Slotted/Drilled Disc: Dilengkapi banyak lubang atau alur di permukaan piringan cakram untuk membantu pelepasan panas dan air. Cakram jenis ini umum digunakan pada kendaraan berperforma tinggi, seperti mobil balap.</p>
      <h3 class="text-start">2. Kaliper Rem (Brake Caliper)</h3>
      <p class="text-start">Kaliper juga menjadi komponen Rem Cakram yang utama dan sebagai pembeda antara cakram dengan tromol. Cara kerja kaliper yaitu dengan mengubah tekanan hidrolik menjadi tekanan mekanis untuk menjepit kampas rem ke cakram. Dalam proses ini, kaliper dibantu tenaga yang disalurkan oleh minyak rem melalui selang hidrolik (Brake Line).</p>
      <p class="text-start">Kaliper dapat dibedakan menjadi dua tipe, yaitu floating caliper dan fixed caliper. Floating caliper memiliki satu piston dan dapat bergerak ke kiri atau kanan untuk menekan kampas dari dua sisi. Sedangkan fixed caliper tidak dapat bergerak karena sudah menyatu dengan brake support, sehingga tugas penekanan hanya di sisi yang memiliki piston.</p>
      <h3 class="text-start">3. Piston Brake</h3>
      <p class="text-start">Komponen berbentuk silinder kecil yang terletak di dalam kaliper. Perannya untuk menerima tekanan hidrolik dari master rem lalu mendorong kampas rem ke arah cakram. Jumlah piston bisa satu, dua, atau bahkan delapan, tergantung pada kebutuhan daya pengereman.</p>
      <p class="text-start">Piston brake dilengkapi dengan seal piston yang berfungsi untuk menarik kembali piston ke posisi semula dengan cepat dan tepat. Selain itu, seal piston juga berperan untuk menutup aliran minyak dan mencegah kebocoran minyak rem.</p>
      <h3 class="text-start">4. Kampas Rem (Brake Pad)</h3>
      <p class="text-start">Kampas Rem atau Brake Pad juga menjadi salah satu komponen Rem Cakram mobil yang paling penting. Komponen inilah yang akan bergesekan langsung dengan piringan cakram. Jadi, fungsi kampas rem ini yaitu untuk menghasilkan gesekan terhadap cakram untuk mengurangi kecepatan putar roda.</p>
      <p class="text-start">Seiring pemakaian, Kampas Rem dapat mengalami keausan sehingga perlu diganti secara berkala. Berdasarkan materialnya, Kampas Rem mobil dibagi menjadi 3 jenis, yaitu:</p>
      <p class="text-start">• Organic (Non-Asbestos): Lembut dan tidak bising, tetapi cepat habis.</p>
      <p class="text-start">• Semi-Metallic: Material jenis ini paling umum digunakan, kuat dan tahan panas.</p>
      <p class="text-start">• Ceramic: Kampas Rem mobil dengan material ceramic mungkin memakan biaya lebih, tetapi sepadan karena sangat tahan panas dan minim debu.</p>
      <h3 class="text-start">5. Bracket Kaliper (Caliper Bracket)</h3>
      <p class="text-start">Bracket kaliper adalah dudukan tempat kaliper terpasang, yang juga menahan Kampas Rem di tempatnya. Komponen ini menyediakan struktur pendukung bagi kaliper agar tetap stabil saat pengereman terjadi dan juga berfungsi sebagai jalur gerak bagi kaliper floating.</p>
      <h3 class="text-start">6. Selang Hidrolik (Brake Hose)</h3>
      <p class="text-start">Brake hose pada sistem Rem Cakram adalah selang yang berfungsi menyalurkan fluida rem (minyak rem) dari master silinder ke kaliper rem. Komponen ini merupakan bagian penting karena berfungsi untuk menyalurkan tekanan hidrolik hasil injakan pedal rem ke piston kaliper.</p>
      <h3 class="text-start">7. Master Silinder Rem (Brake Master Cylinder)</h3>
      <p class="text-start">Komponen Rem Cakram mobil yang satu ini adalah komponen utama yang mengubah tenaga mekanis dari pedal rem menjadi tekanan hidrolik. Bertugas mendorong fluida rem ke seluruh sistem saat pedal diinjak.</p>
      <h3 class="text-start">8. Reservoir Minyak Rem</h3>
      <p class="text-start">Wadah atau tangki berisi fluida rem yang terletak di atas master silinder rem. Fungsinya untuk menyimpan cadangan minyak rem dan memastikan tekanan selalu tersedia di dalam sistem hidrolik.</p>

      <p class="text-start">Itu dia penjelasan mengenai komponen Rem Cakram mobil sekaligus fungsinya. Dalam sistem Rem Cakram, komponen yang paling cepat aus adalah Brake Pad atau Kampas Rem. Hal ini karena Kampas Rem bekerja dengan menghasilkan gesekan langsung terhadap cakram (Disc Rotor) untuk memperlambat atau menghentikan kendaraan. Gesekan ini menyebabkan material pada Kampas Rem secara alami terkikis setiap kali rem digunakan.</p>
      <p class="text-start">Pastikan Anda segera mengganti Kampas Rem mobil yang sudah mulai aus demi menjaga keamanan saat berkendara. Ganti dengan Kampas Rem mobil terbaik dari Somuito yang sudah terbukti kualitasnya. Pesan melalui cabang dan dealer resmi PT. Anugerah Idealestari yang tersebar di berbagai kota besar di Indonesia. Lihat daftar distributor Sparepart mobil terdekat dengan lokasi Anda. Atau klik banner di bawah ini untuk menghubungi CS kami.</p>

