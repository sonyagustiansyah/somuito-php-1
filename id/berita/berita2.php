      <h1 class="fw-bold">Komponen Rem Tromol Mobil Dan Fungsinya</h1>
      <img src="../../assets/gambar/berita2.webp" alt="Ilustrasi rem tromol mobil dengan komponen kampas rem dan drum brake" loading="lazy" class="img-fluid" style="width: 700px; height: auto;">
      <p class="lead">Written by: Shalsabilla Jana</p>
      <p class="text-start">Sudahkah Anda familiar dengan istilah Sistem Rem Tromol pada mobil atau yang dikenal juga dengan Drum Brake? Sistem Rem Tromol ini termasuk yang paling sering digunakan pada kendaraan khususnya di roda bagian belakang. Sebagai pengemudi mobil, Anda perlu ketahui komponen Rem Tromol mobil beserta fungsinya.</p>
      <p class="text-start">Karena komponen-komponen Rem Tromol juga termasuk Sparepart mobil yang memiliki fungsi penting terhadap keselamatan saat berkendara. Oleh karena itu, yuk simak lebih lanjut pembahasan lengkap mengenai komponen Rem Tromol mobil hingga fungsinya di bawah ini.</p>
      
      <h2 class="text-start">Komponen Rem Tromol Mobil Serta Fungsinya</h2>
      <p class="text-start">Komponen Rem Tromol terletak di Kaki-Kaki mobil, tepatnya di roda mobil. Berikut ini adalah komponen Rem Tromol mobil serta fungsinya yang perlu Anda ketahui:</p>

      <h3 class="text-start">1. Drum Brake atau Tromol</h3>
      <p class="text-start">Drum Brake atau tromol adalah salah satu komponen Rem Tromol mobil yang paling utama. Terbuat dari baja atau logam dengan permukaan dalam sebagai media gesekan dengan Kampas Rem atau Brake Shoe. Tromol dipasang langsung pada hub roda dan ikut berputar seiring pergerakan roda mobil.</p>
      <p class="text-start">Fungsi utama dari Tromol adalah sebagai permukaan gesek tempat Kampas Rem atau Brake Shoe menekan saat proses pengereman berlangsung. Ketika pengemudi menekan pedal rem, Brake Shoe yang berada di dalam tromol akan terdorong dan menekan permukaan dalam tromol. Proses ini menghasilkan gaya gesek yang memperlambat atau menghentikan putaran roda.</p>
      <h3 class="text-start">2. Brake Shoe</h3>
      <p class="text-start">Brake Shoe atau Sepatu Rem adalah komponen Rem Tromol mobil yang berfungsi sebagai Kampas Rem mobil yang menciptakan gaya gesek terhadap permukaan dalam tromol. Sepatu Rem umumnya terbuat dari pelat logam berbentuk melengkung yang dilapisi dengan material Kampas Rem (brake lining) di bagian luar yang menghadap ke tromol.</p>
      <p class="text-start">Material kampas ini biasanya berupa campuran serat logam dan bahan tahan panas untuk menahan gesekan tinggi. Bentuk melengkung dari Brake Shoe disesuaikan dengan kontur bagian dalam tromol, sehingga kontak gesek bisa terjadi secara optimal.</p>
      <p class="text-start">Letak Brake Shoe berada di dalam tromol, terpasang pada Backing Plate bersama komponen Rem Tromol mobil lainnya seperti silinder roda dan pegas penarik. Saat pedal rem diinjak, Brake Shoe menerima tekanan dari silinder roda lalu terjadi gesekan dengan permukaan tromol. Saat tekanan dilepas, pegas akan menarik Brake Shoe kembali ke posisi awal agar Kampas Rem tidak terus bergesekan.</p>
      <p class="text-start">Selain itu, Brake Shoe juga berperan dalam fungsi rem parkir, karena mekanisme rem tangan akan menarik Sepatu Rem agar tetap menekan tromol saat kendaraan dalam kondisi parkir.</p>
      <h3 class="text-start">3. Brake Shoe Holder</h3>
      <p class="text-start">Brake Shoe holder adalah komponen yang berfungsi sebagai penyangga atau dudukan bagi Brake Shoe (Sepatu Rem) dalam sistem Rem Tromol. Umumnya, Brake Shoe holder juga terbuat dari logam baja yang kuat dan tahan terhadap tekanan mekanis serta panas tinggi dari proses pengereman.</p>
      <p class="text-start">Brake Shoe holder terletak pada bagian dalam sistem rem tromol, menyatu dengan Backing Plate. Fungsinya adalah untuk menjaga posisi Brake Shoe tetap stabil dan presisi saat proses pengereman berlangsung, serta memastikan Kampas Rem dapat menekan tromol secara merata.</p>
      <p class="text-start">Selain itu, Brake Shoe holder ini juga menopang berbagai komponen lain seperti pegas penarik, adjuster, dan silinder roda. Dengan begitu, keseluruhan sistem Rem Tromol dapat bekerja secara sinkron dan efisien.</p>
      <h3 class="text-start">4. Backing Plate</h3>
      <p class="text-start">Backing Plate merupakan komponen struktural dalam sistem Rem Tromol yang berfungsi sebagai fondasi tempat menempel dan berfungsinya seluruh komponen rem. Komponen ini umumnya terbuat dari bahan baja atau logam pelat yang kuat dan tahan terhadap panas, karena harus menopang komponen rem sekaligus menahan gaya-gaya mekanis selama proses pengereman. Bentuk Backing Plate biasanya bulat dan pipih dengan lubang dan dudukan yang dirancang khusus untuk pemasangan komponen Rem Tromol mobil.</p>
      <p class="text-start">Letaknya berada di belakang tromol, menempel langsung pada knuckle atau bagian sistem suspensi roda. Selain sebagai tempat pemasangan, Backing Plate juga memiliki fungsi membantu dalam menjaga keselarasan komponen rem agar bekerja secara optimal. Jika backing plate mengalami deformasi atau korosi, maka sistem pengereman bisa terganggu karena posisi komponen-komponen di dalamnya menjadi tidak akurat. Oleh karena itu, Backing Plate juga menjadi komponen Rem Tromol mobil yang memiliki peranan penting dalam kestabilan sistem rem.</p>
      <h3 class="text-start">5. Wheel Cylinder (Master Rem Belakang)</h3>
      <p class="text-start">Wheel Cylinder juga dikenal dengan Master Rem Belakang adalah komponen hidrolik yang berperan penting dalam mengubah tekanan minyak rem menjadi gaya mekanis untuk mendorong Brake Shoe. Bentuk Wheel Cylinder menyerupai tabung silinder berukuran kecil yang di dalamnya terdapat dua piston kecil. Di kedua ujung piston tersebut terdapat karet pelindung atau seal untuk mencegah kebocoran minyak rem dan masuknya kotoran.</p>
      <p class="text-start">Wheel cylinder terletak di bagian atas dalam tromol, tepat di antara kedua Brake Shoe, dan langsung menempel pada Backing Plate. Fungsinya sangat krusial, yaitu untuk menerima tekanan hidrolik dari Master Cylinder dan mendorong kedua piston ke arah luar, sehingga Kampas Rem terdorong menekan permukaan dalam tromol.</p>
      <p class="text-start">Kondisi Wheel Cylinder yang bocor atau macet dapat menyebabkan pengereman menjadi lemah atau bahkan tidak berfungsi, sehingga perawatannya sangat penting untuk keselamatan berkendara.</p>
      
      <p class="text-start">Itu dia penjelasan mengenai komponen Rem Tromol mobil beserta fungsinya. Salah satu kelebihan dari sistem Rem Tromol adalah dari desainnya. Karena bentuknya yang tertutup, tromol dapat memberikan perlindungan terhadap komponen rem dari debu dan kotoran luar.  Dari beberapa komponen Rem Tromol mobil yang telah disebutkan di atas, yang paling cepat aus adalah Brake Shoe atau Kampas Rem Tromol.</p>
      <p class="text-start">Jika Anda mendapati Brake Shoe mobil Anda sudah aus, yang ditandai dengan rem tidak pakem, segeralah ganti baru dengan produk yang berkualitas tinggi. Salah satu rekomendasi merk Kampas Rem Tromol terbaik adalah Somuito.</p>
